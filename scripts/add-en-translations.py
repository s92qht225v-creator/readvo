# ── Process files ──────────────────────────────────────────────────────────

missing_vocab = set()
missing_extra = set()
missing_subtitle = set()
missing_grammar_title = set()
missing_grammar_desc = set()

files = sorted(
    glob.glob('content/dialogues/hsk1/dialogue*.json'),
    key=lambda f: int(re.search(r'dialogue(\d+)', f).group(1))
)

for filepath in files:
    with open(filepath) as fh:
        d = json.load(fh)

    modified = False

    # Add en to vocab
    for v in d.get('vocab', []):
        if 'en' not in v:
            en = VOCAB_EN.get(v['ru'])
            if en:
                v['en'] = en
                modified = True
            else:
                missing_vocab.add(v['ru'])
        if 'ex_en' not in v and v.get('ex_ru'):
            # For example sentences, we skip for now (they need manual translation)
            pass

    # Add en to extraVocab
    for e in d.get('extraVocab', []):
        if 'en' not in e:
            en = EXTRA_EN.get(e['ru'])
            if en:
                e['en'] = en
                modified = True
            else:
                missing_extra.add(e['ru'])

    # Add extraVocabSubtitle_en
    if d.get('extraVocabSubtitle_ru') and 'extraVocabSubtitle_en' not in d:
        en = SUBTITLE_EN.get(d['extraVocabSubtitle_ru'])
        if en:
            d['extraVocabSubtitle_en'] = en
            modified = True
        else:
            missing_subtitle.add(d['extraVocabSubtitle_ru'])

    # Add en to grammarNotes
    for g in d.get('grammarNotes', []):
        if 'title_en' not in g:
            en = GRAMMAR_TITLE_EN.get(g['title_ru'])
            if en:
                g['title_en'] = en
                modified = True
            else:
                missing_grammar_title.add(g['title_ru'])
        # Grammar descriptions are longer, track missing
        if 'desc_en' not in g:
            missing_grammar_desc.add(g['desc_ru'])

    if modified:
        with open(filepath, 'w') as fh:
            json.dump(d, fh, ensure_ascii=False, indent=2)
            fh.write('\n')

